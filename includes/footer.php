<script>
    (function () {
        var $phone = window.jQuery ? $('#jp-phone') : null;
        if (!$phone || !$phone.length) {
            return;
        }

        var $send = $('#jp-send-otp');
        var $otpStep = $('#jp-otp-step');
        var $otpInput = $('#jp-otp');
        var $verify = $('#jp-verify-otp');
        var $help = $('#jp-otp-help');
        var $form = $('#jp-signup-form');
        var $status = $('#jp-signup-status');
        var $modalStatus = $('#jp-signup-status-modal');
        var $changeNumber = $('#jp-change-number');
        var $timer = $('#jp-otp-timer');
        var $joinUs = $('#jp-joinus');
        var resendWait = 30;
        var resendInterval = null;
        var resendRemaining = 0;
        var otpVerified = false;

        function showHelp(message, isError) {
            if (!$help.length) {
                return;
            }
            $help.text(message);
            $help.css('color', isError ? '#ffb3b3' : '');
        }

        function normalizePhone(value) {
            var digits = (value || '').replace(/\D+/g, '');
            if (digits.length > 10) {
                digits = digits.slice(-10);
            }
            return digits;
        }

        function isValidPhone(value) {
            return /^[0-9]{10}$/.test(value || '');
        }

        function resetResendTimer() {
            if (resendInterval) {
                clearInterval(resendInterval);
                resendInterval = null;
            }
            resendRemaining = 0;
            if ($timer.length) {
                $timer.text('');
            }
        }

        function startResendTimer() {
            resetResendTimer();
            resendRemaining = resendWait;
            if ($timer.length) {
                $timer.text('Resend OTP in ' + resendRemaining + 's');
            }
            $send.prop('disabled', true).text('Resend OTP');
            resendInterval = setInterval(function () {
                resendRemaining -= 1;
                if (resendRemaining <= 0) {
                    resetResendTimer();
                    $send.prop('disabled', false).text('Resend OTP');
                    return;
                }
                if ($timer.length) {
                    $timer.text('Resend OTP in ' + resendRemaining + 's');
                }
            }, 1000);
        }

        function showStatus(message, isError) {
            if (!$status.length) {
                return;
            }
            $status.text(message);
            $status.css('color', isError ? '#ffb3b3' : '#c9f7c6');
        }

        function showAlert(message, type) {
            if (window.Swal) {
                Swal.fire({
                    icon: type,
                    text: message,
                    confirmButtonColor: '#ffb62f'
                });
                return;
            }
            if (type === 'success') {
                showStatus(message, false);
            } else {
                showStatus(message, true);
            }
        }

        $phone.on('input', function () {
            var normalized = normalizePhone($phone.val());
            $phone.val(normalized);
        });

        $send.on('click', function () {
            var phone = normalizePhone($.trim($phone.val()));
            if (!isValidPhone(phone)) {
                showHelp('Please enter a valid 10-digit mobile number.', true);
                return;
            }

            otpVerified = false;
            $send.prop('disabled', true).text('Sending...');
            $.ajax({
                url: 'ajax/otp.php',
                method: 'POST',
                dataType: 'json',
                data: { action: 'send', phone: phone }
            }).done(function (res) {
                if (res && res.ok) {
                    showHelp(res.message || 'OTP sent.', false);
                    $otpStep.slideDown(150);
                    startResendTimer();
                    showAlert(res.message || 'OTP sent.', 'success');
                } else {
                    showHelp((res && res.message) || 'Unable to send OTP.', true);
                    showAlert((res && res.message) || 'Unable to send OTP.', 'error');
                }
            }).fail(function () {
                showHelp('Unable to send OTP right now.', true);
                showAlert('Unable to send OTP right now.', 'error');
            }).always(function () {
                if (resendRemaining <= 0) {
                    $send.prop('disabled', false).text('Send OTP');
                }
            });
        });

        $verify.on('click', function () {
            var phone = normalizePhone($.trim($phone.val()));
            var code = $.trim($otpInput.val());
            if (code === '') {
                showHelp('Please enter the OTP.', true);
                return;
            }
            if (!isValidPhone(phone)) {
                showHelp('Please enter a valid 10-digit mobile number.', true);
                return;
            }

            $verify.prop('disabled', true).text('Verifying...');
            $.ajax({
                url: 'ajax/otp.php',
                method: 'POST',
                dataType: 'json',
                data: { action: 'verify', phone: phone, code: code }
            }).done(function (res) {
                if (res && res.ok) {
                    otpVerified = true;
                    showHelp('OTP verified. Please complete the form below.', false);
                    $('#jp-signup-modal').modal('show');
                    showAlert('OTP verified. Please complete the form.', 'success');
                } else {
                    showHelp((res && res.message) || 'Invalid OTP.', true);
                    showAlert((res && res.message) || 'Invalid OTP.', 'error');
                }
            }).fail(function () {
                showHelp('Unable to verify OTP right now.', true);
                showAlert('Unable to verify OTP right now.', 'error');
            }).always(function () {
                $verify.prop('disabled', false).text('Verify');
            });
        });

        $changeNumber.on('click', function () {
            $otpInput.val('');
            $phone.val('').focus();
            $otpStep.hide();
            resetResendTimer();
            $send.prop('disabled', false).text('Send OTP');
            otpVerified = false;
            showHelp('Enter your mobile number to get a new OTP.', false);
        });

        $joinUs.on('click', function () {
            $('#jp-signup-modal').modal('show');
        });

        $form.on('submit', function (e) {
            e.preventDefault();
            showStatus('', false);
            if ($modalStatus.length) {
                $modalStatus.text('');
            }

            var formData = $form.serialize();
            $.ajax({
                url: 'ajax/signup.php',
                method: 'POST',
                dataType: 'json',
                data: formData
            }).done(function (res) {
                if (res && res.ok) {
                    showStatus('Thanks! Your details have been submitted.', false);
                    if ($modalStatus.length) {
                        $modalStatus.text('Thanks! Your details have been submitted.').css('color', '#c9f7c6');
                    }
                    showAlert('Thanks! Your details have been submitted.', 'success');
                    $form[0].reset();
                } else {
                    showStatus((res && res.message) || 'Unable to submit details.', true);
                    if ($modalStatus.length) {
                        $modalStatus.text((res && res.message) || 'Unable to submit details.').css('color', '#ffb3b3');
                    }
                    showAlert((res && res.message) || 'Unable to submit details.', 'error');
                }
            }).fail(function (xhr) {
                var message = 'Unable to submit details right now.';
                if (xhr && xhr.responseJSON && xhr.responseJSON.message) {
                    message = xhr.responseJSON.message;
                }
                showStatus(message, true);
                if ($modalStatus.length) {
                    $modalStatus.text(message).css('color', '#ffb3b3');
                }
                showAlert(message, 'error');
            });
        });
    })();
</script>
<script>
    (function () {
        var $form = window.jQuery ? $('#jp-counselling-form') : null;
        if (!$form || !$form.length) {
            return;
        }

        $form.on('submit', function (e) {
            e.preventDefault();
            var $btn = $form.find('button[type="submit"]');
            $btn.prop('disabled', true).text('Submitting...');
            $.ajax({
                url: $form.attr('action'),
                method: 'POST',
                dataType: 'json',
                data: $form.serialize()
            }).done(function (res) {
                if (res && res.ok) {
                    if (window.Swal) {
                        Swal.fire({ icon: 'success', text: res.message });
                    }
                    $form[0].reset();
                } else if (window.Swal) {
                    Swal.fire({ icon: 'error', text: (res && res.message) || 'Unable to submit.' });
                }
            }).fail(function (xhr) {
                var message = 'Unable to submit right now.';
                if (xhr && xhr.responseJSON && xhr.responseJSON.message) {
                    message = xhr.responseJSON.message;
                }
                if (window.Swal) {
                    Swal.fire({ icon: 'error', text: message });
                }
            }).always(function () {
                $btn.prop('disabled', false).text('Request Callback');
            });
        });
    })();
</script>
